fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let a: Vec<u32> = tokens.collect(n);
    debug!(a);

    let mut la = vec![0; n];
    let mut lv = 1;
    for i in 0..n {
        if a[i] >= lv {
            la[i] = lv;
            lv += 1;
        }
        else {
            la[i] = a[i];
            lv = a[i] + 1;
        }
    }

    let mut ra = vec![0; n];
    let mut rv = 1;
    for i in (0..n).rev() {
        if a[i] >= rv {
            ra[i] = rv;
            rv += 1;
        }
        else {
            ra[i] = a[i];
            rv = a[i] + 1;
        }
    }

    debug!(la, ra);

    let mut ma = vec![0; n];
    for i in 0..n {
        ma[i] = la[i].min(ra[i]);
    }

    debug!(ma);

    ma.sort();
    let k = ma[n - 1];
    println!("{k}");
}
