fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let t: usize = tokens.next();

    let mut v = vec![0; n];
    let mut m = BTreeMap::new();
    m.insert(0, n);

    let mut k = 1;

    for _ in 0..t {
        let a: usize = tokens.next();
        let b: u64 = tokens.next();

        let a = a - 1;

        let s0 = v[a];
        v[a] += b;
        let s1 = v[a];
        debug!(v);

        if m[&s0] == 1 {
            m.remove(&s0);
            k -= 1;
        }
        else {
            m.insert(s0, m[&s0] - 1);
        }

        if m.contains_key(&s1) {
            m.insert(s1, m[&s1] + 1);
        }
        else {
            m.insert(s1, 1);
            k += 1;
        }
        debug!(m);

        println!("{k}");
    }
}
