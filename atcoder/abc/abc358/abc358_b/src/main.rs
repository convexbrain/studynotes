fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let a: usize = tokens.next();
    let mut v: VecDeque<usize> = tokens.collect(n);

    let mut q = VecDeque::new();

    loop {
        debug!(v, q);
        let v0 = v.front();
        let q0 = q.front();
        if v0.is_some() && q0.is_some() {
            let v0 = v0.unwrap();
            let q0 = q0.unwrap();
            if v0 < q0 {
                v.pop_front();
                q.push_back(q.back().unwrap() + a);
            }
            else {
                println!("{q0}");
                q.pop_front();
            }
        }
        else if v0.is_some() {
            q.push_back(v0.unwrap() + a);
            v.pop_front();
        }
        else if q0.is_some() {
            println!("{}", q0.unwrap());
            q.pop_front();
        }
        else {
            break;
        }
    }
}
