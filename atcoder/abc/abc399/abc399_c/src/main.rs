fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let m: usize = tokens.next();

    let mut g = vec![BTreeSet::new(); n];
    for i in 0..m {
        let u: usize = tokens.next();
        let v: usize = tokens.next();
        let u = u - 1;
        let v = v - 1;
        g[u].insert((i, v));
        g[v].insert((i, u));
    }

    let mut unvis = BTreeSet::from_iter(0..n);
    let mut uncut = BTreeSet::from_iter(0..m);
    while let Some(start) = unvis.first() {
        let mut que = VecDeque::new();
        que.push_back((*start, None));
        while let Some(n) = que.pop_front() {
            if unvis.contains(&n.0) {
                unvis.remove(&n.0);
                if let Some(edge) = n.1 {
                    uncut.remove(&edge);
                }
                for next in g[n.0].iter() {
                    que.push_back((next.1, Some(next.0)));
                }
            }
        }
    }
    println!("{}", uncut.len());
}
