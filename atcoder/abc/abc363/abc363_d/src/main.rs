fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();

    let mut nn = n - 1;

    if nn < 10 {
        println!("{nn}");
        return;
    }
    nn -= 10;

    if nn < 9 {
        println!("{}{}", nn + 1, nn + 1);
        return;
    }
    nn -= 9;

    let mut p = 10;
    for dh in 2.. {
        let m = p * 10 - p;
        for ev in 0..2 {
            if nn < m {
                let head = format!("{}", p + nn);
                print!("{}", head);
                let mut tail = head.chars().rev();
                if ev == 0 {
                    tail.next();
                }
                while let Some(c) = tail.next() {
                    print!("{}", c);
                }
                println!();
                return;
            }
            nn -= m;
        }
        p *= 10;
    }

}
