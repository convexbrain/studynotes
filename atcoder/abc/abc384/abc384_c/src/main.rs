fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let abcde: Vec<u32> = tokens.collect(5);

    let mut bh = BinaryHeap::new();

    for i in 1..=31 {
        let mut n = String::new();
        let mut s = 0;
        for j in 0..5 {
            if i & (1 << j) != 0 {
                let b0 = char::from_u32((b'A' + j) as u32).unwrap(); // u8 -> char
                n.push(b0);
                s += abcde[j as usize];
            }
        }
        bh.push((s, Reverse(n)));
    }
    debug!(bh);

    while let Some(bhi) = bh.pop() {
        println!("{}", bhi.1.0);
    }
}
