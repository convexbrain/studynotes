fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let mut a: Vec<u64> = tokens.collect(n);

    let sum = a.iter().sum::<u64>() * (n as u64 - 1);
    a.sort();

    let mut s =  BTreeMap::new();
    for (idx, ai) in a.iter().enumerate() {
        if !s.contains_key(ai) {
            s.insert(*ai, idx);
        }
    }
    //let s: BTreeMap<u64, usize> = a.iter().copied().enumerate().map(|x| (x.1, x.0)).collect();
    debug!(s);

    let mut c = 0;
    for ai in a.iter() {
        if let Some(r) = s.range((100000000 - *ai)..).next() {
            c += n - r.1;

            if ai * 2 >= 100000000 {
                c -= 1;
            }
        }
    }
    debug!(sum, c);
    c /= 2;

    let ans = sum - c as u64 * 100000000;
    println!("{ans}");
}
