fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let h: Vec<i64> = tokens.collect(n);

    let mut t = 0;
    for hi in h.iter() {
        let mut hh = *hi;
        debug!(hh, t);

        loop {
            t += 1;
            if t % 3 == 0 {
                hh -= 3;
                break;
            }
            else {
                hh -= 1;
                if hh < 1 {
                    break;
                }
            }
        }
        debug!(hh, t);

        if hh < 1 {
            continue;
        }

        let q = hh / 5;
        let r = hh % 5;

        t += q * 3;
        t += match r {
            0 => {0},
            1 => {1},
            2 => {2},
            _ => {3},
        };
        debug!(t);
    }
    println!("{t}");
}
