fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let n: usize = tokens.next();
    let k: u32 = tokens.next();
    let r: Vec<u32> = tokens.collect(n);

    for i in 0..(5_u32.pow(n as u32)) {
        let mut a = VecDeque::new();
        let mut sum = 0;
        let mut ok = true;

        for j in 0..n {
            let d = i / (5_u32.pow((n - 1 - j) as u32));
            let d = d % 5 + 1;
            if d < 1 || r[j] < d {
                ok = false;
                break;
            }
            sum += d;
            a.push_back(d);
        }

        if ok && sum % k == 0 {
            //debug!(a);
            while let Some(d) = a.pop_front() {
                print!("{d} ");
            }
            println!();
        }
    }
}
