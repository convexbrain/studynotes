fn main() {
    let mut tokens_buf = String::new();
    let mut tokens = Tokens::new(&mut tokens_buf);

    let a: i64 = tokens.next();
    let m: i64 = tokens.next();
    let l: i64 = tokens.next();
    let r: i64 = tokens.next();
    debug!(a, m, l, r);

    if l == r && (l - a) % m != 0{
        println!("0");
        return;
    }

    let kl = l - a;
    let kr = r - a;
    debug!(kl, kr);

    //let kl = kl / m;
    //let kr = kr / m;
    let kl = if kl < 0 {-((-kl) / m)} else {(kl + m - 1) / m};
    let kr = if kr < 0 {-((-kr + m - 1) / m)} else {(kr) / m};
    debug!(kl, kr);

    let ans = kr - kl + 1;
    println!("{ans}");
}
